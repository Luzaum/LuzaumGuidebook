/**
 * Gera objetos FoodItem para Guabi Natural (abr 2026) e imprime JSON array.
 * Uso: node scripts/gen-guabi-natural-foods.mjs > tmp-guabi.json
 */
const DM = 91 // % matéria seca (umidade 9%)

function r6(x) {
  if (x == null || typeof x !== 'number' || Number.isNaN(x)) return x
  return Math.round(x * 1e6) / 1e6
}

function ms(af) {
  if (af == null) return null
  return r6((af * 100) / DM)
}

function enrich(kgPerKg) {
  return r6(kgPerKg / 10)
}

function roundDeep(obj) {
  if (obj == null || typeof obj !== 'object') return obj
  if (Array.isArray(obj)) return obj.map(roundDeep)
  const out = {}
  for (const [k, v] of Object.entries(obj)) {
    out[k] = typeof v === 'number' ? r6(v) : roundDeep(v)
  }
  return out
}

function baseFood({
  id,
  name,
  presentation,
  speciesScope,
  moisturePct,
  energyKcalKg,
  pb,
  ee,
  fiber,
  ash,
  calciumPct, // single value MN
  phosphorusPct,
  potassiumGPerKg, // 6 ou 4.5
  sodiumGPerKg,
  lysineGPerKg,
  taurineMgPerKg,
  methionineGPerKg,
  carnitineMgPerKg,
  omega6GPerKg,
  omega3GPerKg,
  epaDhaMgPerKg,
  tryptophanGPerKg,
  enrichment,
  notes,
}) {
  const dryMatterPct = 100 - moisturePct
  const nfe = r6(100 - moisturePct - pb - ee - fiber - ash)
  const energyKcalPer100g = energyKcalKg / 10

  const taurinePct = taurineMgPerKg / 10000
  const lysinePct = lysineGPerKg / 10
  const methioninePct = methionineGPerKg / 10
  const tryptophanPct = tryptophanGPerKg != null ? tryptophanGPerKg / 10 : null
  const omega6Pct = omega6GPerKg / 10
  const omega3Pct = omega3GPerKg / 10
  const epaDhaPct = epaDhaMgPerKg / 10000
  const potassiumPct = potassiumGPerKg / 10
  const sodiumPct = sodiumGPerKg != null ? sodiumGPerKg / 10 : null

  const e = enrichment

  const nutrientsAsFed = {
    moisturePct,
    dryMatterPct,
    energyKcalPer100g,
    crudeProteinPct: pb,
    etherExtractPct: ee,
    ashPct: ash,
    crudeFiberPct: fiber,
    nitrogenFreeExtractPct: Math.round(nfe * 1000) / 1000,
    calciumPct,
    phosphorusPct,
    potassiumPct,
    sodiumPct,
    chloridePct: null,
    magnesiumPct: null,
    manganeseMg: e.mn != null ? enrich(e.mn) : null,
    copperMg: e.cu != null ? enrich(e.cu) : null,
    zincMg: e.zn != null ? enrich(e.zn) : null,
    seleniumMg: e.se != null ? enrich(e.se) : null,
    vitaminAIu: e.a != null ? enrich(e.a) : null,
    vitaminDIu: e.d != null ? enrich(e.d) : null,
    vitaminEIu: e.e != null ? enrich(e.e) : null,
    thiamineMg: e.b1 != null ? enrich(e.b1) : null,
    riboflavinMg: e.b2 != null ? enrich(e.b2) : null,
    pyridoxineMg: e.b6 != null ? enrich(e.b6) : null,
    taurinePct,
    methionineCystinePct: null,
    omega3Pct,
    omega6Pct,
    mcfaPct: null,
    cholineMg: e.choline != null ? enrich(e.choline) : null,
    ironMg: e.fe != null ? enrich(e.fe) : null,
    iodineMg: e.i != null ? enrich(e.i) : null,
    niacinMg: e.niacin != null ? enrich(e.niacin) : null,
    pantothenicAcidMg: e.pant != null ? enrich(e.pant) : null,
    biotinMg: e.bio != null ? enrich(e.bio) : null,
    folicAcidMg: e.folic != null ? enrich(e.folic) : null,
    cobalaminMcg: e.b12 != null ? enrich(e.b12) : null,
    vitaminKMg: e.k != null ? enrich(e.k) : null,
    lysinePct,
    methioninePct,
    tryptophanPct,
    epaPct: null,
    dhaPct: null,
    epaDhaPct,
    carnitineMgPerKg: carnitineMgPerKg,
    vitaminCMgPerKg: e.c != null ? e.c : null,
  }

  const nutrientsDryMatter = Object.fromEntries(
    Object.entries(nutrientsAsFed).map(([k, v]) => [k, v == null ? null : ms(v)]),
  )
  nutrientsDryMatter.moisturePct = 0
  nutrientsDryMatter.dryMatterPct = 100

  const missingNutrients = [
    'chloridePct',
    'epaPct',
    'dhaPct',
    'magnesiumPct',
    'mcfaPct',
    'methionineCystinePct',
    'phenylalaninePct',
    'sodiumPct',
  ].filter((k) => {
    if (k === 'sodiumPct' && nutrientsAsFed.sodiumPct != null) return false
    return nutrientsAsFed[k] == null
  })

  if (tryptophanPct == null) missingNutrients.push('tryptophanPct')

  const food = {
    id,
    slug: id,
    name,
    category: 'Ração',
    categoryNormalized: 'Ração',
    sourceSheet: 'Alimentos MN',
    sourceReference: {
      workbook: 'Guabi Natural — rótulo BR / tabela usuário abr 2026',
      mnRow: 0,
      msRow: null,
    },
    speciesScope,
    foodType: 'commercial',
    presentation,
    nutrientsAsFed,
    nutrientsDryMatter,
    missingNutrients: [...new Set(missingNutrients)],
    notes,
  }
  food.nutrientsAsFed = roundDeep(food.nutrientsAsFed)
  food.nutrientsDryMatter = roundDeep(food.nutrientsDryMatter)
  return food
}

