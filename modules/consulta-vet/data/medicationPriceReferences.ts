export function withMedicationPriceReference<T extends MedicationRecord>(record: T): T {
  if (record.priceReference) return record;
  const priceReference = medicationPriceReferencesBySlug[record.slug];
  return priceReference ? { ...record, priceReference } : record;
}

export function withMedicationPriceReferences<T extends MedicationRecord>(records: T[]): T[] {
  return records.map(withMedicationPriceReference);
}
