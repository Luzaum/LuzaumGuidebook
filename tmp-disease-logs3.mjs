import { chromium } from 'playwright';
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage();
const logs=[];
const failed=[];
page.on('console', (msg) => logs.push({type:msg.type(), text:msg.text()}));
page.on('requestfailed', (req) => failed.push({url:req.url(), error:req.failure()?.errorText}));
await page.goto('http://127.0.0.1:5173/consulta-vet/doencas/cinomose-canina', { waitUntil: 'domcontentloaded', timeout: 30000 });
await page.waitForTimeout(12000);
const spinner = await page.locator('.animate-spin').count();
console.log(JSON.stringify({logs, failed, spinner}, null, 2));
await browser.close();
